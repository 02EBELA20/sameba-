/** სწრაფი lookup */
export const getDevotionalVerseById = (id: number | string): DevotionalVerse | null => {
  const n = Number(id);
  if (Number.isNaN(n)) return null;
  return DEVOTIONAL_VERSES.find((v) => v.id === n) ?? null;
};

/**
 * Random მუხლი (კი, მაგრამ შეგიძლია “seen”-ის მიხედვით თავიდან აიცილო გამეორება)
 * seenIds = [2, 5, 8] ...
 */
export const getRandomDevotionalVerse = (seenIds: number[] = []): DevotionalVerse => {
  const seen = new Set(seenIds);
  const pool = DEVOTIONAL_VERSES.filter((v) => !seen.has(v.id));
  const list = pool.length ? pool : DEVOTIONAL_VERSES; // თუ ყველა “ნანახია” — თავიდან იწყებს
  return list[Math.floor(Math.random() * list.length)];
};

/** Home-ზე მოკლე preview რომ ლამაზად გამოჩნდეს */
export const getVersePreview = (text: string, max = 120) => {
  const s = String(text ?? "").replace(/\s+/g, " ").trim();
  if (s.length <= max) return s;
  return s.slice(0, max - 1) + "…";
};
